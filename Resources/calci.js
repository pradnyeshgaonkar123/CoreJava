function add(num1,num2){
	print("Addition is : "+parseInt(num1 + num2));
}
function sub(num1,num2){
	print("Subtraction is : "+parseInt(num1 - num2));
}
function mul(num1,num2){
	print("Multiplication is : "+parseInt(num1 * num2));
}
function div(num1,num2){
	print("Division is : "+parseInt(num1 / num2));
}